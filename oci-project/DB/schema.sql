CREATE INDEX IDX_DEPLOY_STATUS  ON DEPLOYMENTS(STATUS);

---------------------------------------------------------------
-- 16 INCIDENTS
---------------------------------------------------------------
CREATE TABLE INCIDENTS (
    INCIDENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROJECT_ID  NUMBER,
    TYPE        VARCHAR2(100),
    DESCRIPTION CLOB,
    SEVERITY    VARCHAR2(20)
                CHECK (SEVERITY IN ('LOW','MEDIUM','HIGH','CRITICAL')),
    OCCURRED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    RESOLVED_AT TIMESTAMP,
    IS_DELETED  CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT  TIMESTAMP,

    CONSTRAINT FK_INCIDENT_PROJECT FOREIGN KEY (PROJECT_ID) REFERENCES PROJECTS(PROJECT_ID),
    CONSTRAINT CHK_INCIDENT_DATES  CHECK (RESOLVED_AT IS NULL OR RESOLVED_AT >= OCCURRED_AT)
);

CREATE INDEX IDX_INCIDENTS_PROJECT  ON INCIDENTS(PROJECT_ID);
CREATE INDEX IDX_INCIDENTS_SEVERITY ON INCIDENTS(SEVERITY);

---------------------------------------------------------------
-- 17 BOT INTERACTIONS
---------------------------------------------------------------
CREATE TABLE BOT_INTERACTIONS (
    INTERACTION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    USER_ID        NUMBER,
    MESSAGE        CLOB,
    RESPONSE       CLOB,
    CREATED_AT     TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,

    CONSTRAINT PK_BOT_INTERACTIONS PRIMARY KEY (INTERACTION_ID, CREATED_AT),
    CONSTRAINT FK_BOT_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
)
PARTITION BY RANGE (CREATED_AT) INTERVAL (NUMTOYMINTERVAL(1,'MONTH'))
(
    PARTITION P_INITIAL VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00')
);

CREATE INDEX IDX_BOT_USER ON BOT_INTERACTIONS(USER_ID) LOCAL;

