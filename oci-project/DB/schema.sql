CREATE INDEX IDX_TSH_TASK ON TASK_STATUS_HISTORY(TASK_ID);

---------------------------------------------------------------
-- 11 TASK SPRINT HISTORY
---------------------------------------------------------------
CREATE TABLE TASK_SPRINT_HISTORY (
    HISTORY_ID    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TASK_ID       NUMBER NOT NULL,
    OLD_SPRINT_ID NUMBER,           -- NULL cuando se asigna por primera vez
    NEW_SPRINT_ID NUMBER,           -- NULL cuando se quita del sprint
    CHANGED_BY    NUMBER,
    CHANGED_AT    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_TSH_TASK       FOREIGN KEY (TASK_ID)       REFERENCES TASKS(TASK_ID),
    CONSTRAINT FK_TSH_OLD_SPRINT FOREIGN KEY (OLD_SPRINT_ID) REFERENCES SPRINTS(SPRINT_ID),
    CONSTRAINT FK_TSH_NEW_SPRINT FOREIGN KEY (NEW_SPRINT_ID) REFERENCES SPRINTS(SPRINT_ID),
    CONSTRAINT FK_TSH_USER       FOREIGN KEY (CHANGED_BY)    REFERENCES USERS(USER_ID)
);

CREATE INDEX IDX_TSPH_TASK ON TASK_SPRINT_HISTORY(TASK_ID);

---------------------------------------------------------------
-- 12 KPI TYPES
---------------------------------------------------------------
CREATE TABLE KPI_TYPES (
    KPI_TYPE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME        VARCHAR2(150) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR2(500),
    CATEGORY    VARCHAR2(50),
    UNIT        VARCHAR2(50)
);

---------------------------------------------------------------
-- 13 KPI VALUES
---------------------------------------------------------------
CREATE TABLE KPI_VALUES (
    KPI_VALUE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    KPI_TYPE_ID  NUMBER        NOT NULL,

    -- Discriminador: a qué entidad pertenece esta medición
    SCOPE_TYPE   VARCHAR2(20)  NOT NULL
                 CHECK (SCOPE_TYPE IN ('USER','PROJECT','SPRINT','GLOBAL')),

    USER_ID      NUMBER,
    PROJECT_ID   NUMBER,
    SPRINT_ID    NUMBER,

    VALUE        NUMBER(12,4)  NOT NULL,
    RECORDED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_KPI_TYPE    FOREIGN KEY (KPI_TYPE_ID) REFERENCES KPI_TYPES(KPI_TYPE_ID),
    CONSTRAINT FK_KPI_USER    FOREIGN KEY (USER_ID)     REFERENCES USERS(USER_ID),
    CONSTRAINT FK_KPI_PROJECT FOREIGN KEY (PROJECT_ID)  REFERENCES PROJECTS(PROJECT_ID),
    CONSTRAINT FK_KPI_SPRINT  FOREIGN KEY (SPRINT_ID)   REFERENCES SPRINTS(SPRINT_ID),

    -- Garantiza coherencia entre SCOPE_TYPE y FKs presentes
    CONSTRAINT CHK_KPI_SCOPE CHECK (
        (SCOPE_TYPE = 'USER'    AND USER_ID    IS NOT NULL) OR
        (SCOPE_TYPE = 'PROJECT' AND PROJECT_ID IS NOT NULL) OR
        (SCOPE_TYPE = 'SPRINT'  AND SPRINT_ID  IS NOT NULL) OR
        (SCOPE_TYPE = 'GLOBAL')
    )
);

CREATE INDEX IDX_KPI_USER    ON KPI_VALUES(USER_ID);
CREATE INDEX IDX_KPI_PROJECT ON KPI_VALUES(PROJECT_ID);
CREATE INDEX IDX_KPI_SPRINT  ON KPI_VALUES(SPRINT_ID);

---------------------------------------------------------------
-- 14 LLM ANALYSIS
---------------------------------------------------------------
CREATE TABLE LLM_ANALYSIS (
    ANALYSIS_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    SCOPE_TYPE       VARCHAR2(20)  NOT NULL
                     CHECK (SCOPE_TYPE IN ('USER','PROJECT','SPRINT','GLOBAL')),

    USER_ID          NUMBER,
    PROJECT_ID       NUMBER,
    SPRINT_ID        NUMBER,

    ANOMALY_DETECTED CHAR(1)       CHECK (ANOMALY_DETECTED IN ('Y','N')),
    ANOMALY_TYPE     VARCHAR2(100),
    CONFIDENCE_SCORE NUMBER(5,2)
                     CHECK (CONFIDENCE_SCORE BETWEEN 0 AND 100),
    RECOMMENDATION   CLOB,
    ANALYSIS_DATE    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_ANALYSIS_USER    FOREIGN KEY (USER_ID)    REFERENCES USERS(USER_ID),
    CONSTRAINT FK_ANALYSIS_PROJECT FOREIGN KEY (PROJECT_ID) REFERENCES PROJECTS(PROJECT_ID),
    CONSTRAINT FK_ANALYSIS_SPRINT  FOREIGN KEY (SPRINT_ID)  REFERENCES SPRINTS(SPRINT_ID),

    CONSTRAINT CHK_ANALYSIS_SCOPE CHECK (
        (SCOPE_TYPE = 'USER'    AND USER_ID    IS NOT NULL) OR
        (SCOPE_TYPE = 'PROJECT' AND PROJECT_ID IS NOT NULL) OR
        (SCOPE_TYPE = 'SPRINT'  AND SPRINT_ID  IS NOT NULL) OR
        (SCOPE_TYPE = 'GLOBAL')
    )
);

CREATE INDEX IDX_LLM_PROJECT ON LLM_ANALYSIS(PROJECT_ID);
CREATE INDEX IDX_LLM_USER    ON LLM_ANALYSIS(USER_ID);

