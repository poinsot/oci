---------------------------------------------------------------
-- 1  ROLES
---------------------------------------------------------------
CREATE TABLE ROLES (
    ROLE_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLE_NAME   VARCHAR2(50)  NOT NULL UNIQUE,
    DESCRIPTION VARCHAR2(200),
    IS_DELETED  CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT  TIMESTAMP
);

---------------------------------------------------------------
-- 2  TEAMS
---------------------------------------------------------------
CREATE TABLE TEAMS (
    TEAM_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME        VARCHAR2(100) NOT NULL,
    DESCRIPTION VARCHAR2(300),
    IS_DELETED  CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT  TIMESTAMP
);

---------------------------------------------------------------
-- 3  USERS
---------------------------------------------------------------
CREATE TABLE USERS (
    USER_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FULL_NAME   VARCHAR2(150) NOT NULL,
    EMAIL       VARCHAR2(150) NOT NULL UNIQUE,
    TELEGRAM_ID VARCHAR2(100) UNIQUE,
    ROLE_ID     NUMBER        NOT NULL,
    TEAM_ID     NUMBER,
    STATUS      VARCHAR2(20)  DEFAULT 'ACTIVE'
                CHECK (STATUS IN ('ACTIVE','INACTIVE','LOCKED')),
    CREATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT  TIMESTAMP,
    IS_DELETED  CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT  TIMESTAMP,

    CONSTRAINT FK_USER_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES(ROLE_ID),
    CONSTRAINT FK_USER_TEAM FOREIGN KEY (TEAM_ID) REFERENCES TEAMS(TEAM_ID)
);

CREATE INDEX IDX_USERS_TELEGRAM   ON USERS(TELEGRAM_ID);
CREATE INDEX IDX_USERS_EMAIL      ON USERS(EMAIL);
CREATE INDEX IDX_USERS_STATUS     ON USERS(STATUS);

---------------------------------------------------------------
-- 4  USER CREDENTIALS
---------------------------------------------------------------
CREATE TABLE USER_CREDENTIALS (
    CREDENTIAL_ID   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID         NUMBER        NOT NULL UNIQUE,
    USERNAME        VARCHAR2(100) NOT NULL UNIQUE,
    PASSWORD_HASH   VARCHAR2(255) NOT NULL,
    PASSWORD_SALT   VARCHAR2(255) NOT NULL,
    LAST_LOGIN      TIMESTAMP,
    FAILED_ATTEMPTS NUMBER DEFAULT 0,
    ACCOUNT_LOCKED  CHAR(1) DEFAULT 'N' CHECK (ACCOUNT_LOCKED IN ('Y','N')),
    CREATED_AT      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED      CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT      TIMESTAMP,

    CONSTRAINT FK_CREDENTIAL_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);

---------------------------------------------------------------
-- 5  SPRINTS
---------------------------------------------------------------
CREATE TABLE SPRINTS (
    SPRINT_ID  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME       VARCHAR2(100) NOT NULL,
    START_DATE DATE NOT NULL,
    END_DATE   DATE NOT NULL,
    STATUS     VARCHAR2(20)  DEFAULT 'PLANNED'
               CHECK (STATUS IN ('PLANNED','ACTIVE','CLOSED')),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT TIMESTAMP,

    CONSTRAINT CHK_SPRINT_DATES CHECK (END_DATE >= START_DATE)
);

CREATE INDEX IDX_SPRINTS_STATUS ON SPRINTS(STATUS);

---------------------------------------------------------------
-- 6  PROJECTS
---------------------------------------------------------------
CREATE TABLE PROJECTS (
    PROJECT_ID  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME        VARCHAR2(150) NOT NULL,
    DESCRIPTION CLOB,
    MANAGER_ID  NUMBER NOT NULL,
    STATUS      VARCHAR2(20)  DEFAULT 'ACTIVE'
                CHECK (STATUS IN ('ACTIVE','ON_HOLD','COMPLETED','CANCELLED')),
    CREATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT  TIMESTAMP,
    IS_DELETED  CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT  TIMESTAMP,

    CONSTRAINT FK_PROJECT_MANAGER FOREIGN KEY (MANAGER_ID) REFERENCES USERS(USER_ID)
);

CREATE INDEX IDX_PROJECTS_MANAGER ON PROJECTS(MANAGER_ID);
CREATE INDEX IDX_PROJECTS_STATUS  ON PROJECTS(STATUS);

---------------------------------------------------------------
-- 7  PROJECT_SPRINTS
---------------------------------------------------------------
CREATE TABLE PROJECT_SPRINTS (
    PROJECT_ID    NUMBER NOT NULL,
    SPRINT_ID     NUMBER NOT NULL UNIQUE,   -- un sprint pertenece a 1 proyecto
    SPRINT_NUMBER NUMBER NOT NULL,          -- orden relativo al proyecto: 1, 2, 3...
    IS_ACTIVE     CHAR(1) DEFAULT 'N' CHECK (IS_ACTIVE IN ('Y','N')), 
    ASSIGNED_AT   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT PK_PROJECT_SPRINT  PRIMARY KEY (PROJECT_ID, SPRINT_ID),
    CONSTRAINT FK_PS_PROJECT      FOREIGN KEY (PROJECT_ID) REFERENCES PROJECTS(PROJECT_ID),
    CONSTRAINT FK_PS_SPRINT       FOREIGN KEY (SPRINT_ID)  REFERENCES SPRINTS(SPRINT_ID),
    -- Garantiza que no haya dos "Sprint 1" dentro del mismo proyecto
    CONSTRAINT UQ_PS_NUMBER       UNIQUE (PROJECT_ID, SPRINT_NUMBER),
    -- El número de sprint debe ser positivo
    CONSTRAINT CHK_PS_NUMBER      CHECK (SPRINT_NUMBER > 0)
);

CREATE INDEX IDX_PS_PROJECT ON PROJECT_SPRINTS(PROJECT_ID);

---------------------------------------------------------------
-- 8  PROJECT MEMBERS
---------------------------------------------------------------
CREATE TABLE PROJECT_MEMBERS (
    PROJECT_ID      NUMBER NOT NULL,
    USER_ID         NUMBER NOT NULL,
    ROLE_IN_PROJECT VARCHAR2(50),
    JOINED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    IS_DELETED      CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT      TIMESTAMP,

    CONSTRAINT PK_PROJECT_MEMBER PRIMARY KEY (PROJECT_ID, USER_ID),
    CONSTRAINT FK_PM_PROJECT      FOREIGN KEY (PROJECT_ID) REFERENCES PROJECTS(PROJECT_ID),
    CONSTRAINT FK_PM_USER         FOREIGN KEY (USER_ID)    REFERENCES USERS(USER_ID)
);

CREATE INDEX IDX_PM_USER ON PROJECT_MEMBERS(USER_ID);

---------------------------------------------------------------
-- 9  TASKS
---------------------------------------------------------------
CREATE TABLE TASKS (
    TASK_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROJECT_ID  NUMBER,
    SPRINT_ID   NUMBER,
    TITLE       VARCHAR2(200) NOT NULL,
    DESCRIPTION CLOB,

    -- Dónde vive la tarea en el tablero
    TASK_STAGE  VARCHAR2(20)  DEFAULT 'BACKLOG'
                CHECK (TASK_STAGE IN ('BACKLOG','SPRINT','COMPLETED')),

    -- Estado operacional de la tarea
    STATUS      VARCHAR2(30)  DEFAULT 'PENDING'
                CHECK (STATUS IN ('PENDING','IN_PROGRESS','DONE','CANCELLED','REOPENED')),

    PRIORITY    VARCHAR2(20)
                CHECK (PRIORITY IN ('LOW','MEDIUM','HIGH')),

    CREATED_BY  NUMBER NOT NULL,
    ASSIGNED_TO NUMBER,
    CREATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT  TIMESTAMP,
    DUE_DATE    DATE,
    IS_DELETED  CHAR(1) DEFAULT 'N' CHECK (IS_DELETED IN ('Y','N')),
    DELETED_AT  TIMESTAMP,

    CONSTRAINT FK_TASK_PROJECT  FOREIGN KEY (PROJECT_ID)  REFERENCES PROJECTS(PROJECT_ID),
    CONSTRAINT FK_TASK_SPRINT   FOREIGN KEY (SPRINT_ID)   REFERENCES SPRINTS(SPRINT_ID),
    CONSTRAINT FK_TASK_CREATED  FOREIGN KEY (CREATED_BY)  REFERENCES USERS(USER_ID),
    CONSTRAINT FK_TASK_ASSIGNED FOREIGN KEY (ASSIGNED_TO) REFERENCES USERS(USER_ID)
);

CREATE INDEX IDX_TASKS_PROJECT     ON TASKS(PROJECT_ID);
CREATE INDEX IDX_TASKS_SPRINT      ON TASKS(SPRINT_ID);
CREATE INDEX IDX_TASKS_ASSIGNED    ON TASKS(ASSIGNED_TO);
CREATE INDEX IDX_TASKS_STATUS      ON TASKS(STATUS);
CREATE INDEX IDX_TASKS_STAGE       ON TASKS(TASK_STAGE);
CREATE INDEX IDX_TASKS_PRIORITY    ON TASKS(PRIORITY);

---------------------------------------------------------------
-- 10 TASK STATUS HISTORY
---------------------------------------------------------------
CREATE TABLE TASK_STATUS_HISTORY (
    HISTORY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TASK_ID    NUMBER       NOT NULL,
    OLD_STATUS VARCHAR2(30),
    NEW_STATUS VARCHAR2(30) NOT NULL,
    CHANGED_BY NUMBER,
    CHANGED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT FK_HISTORY_TASK FOREIGN KEY (TASK_ID)     REFERENCES TASKS(TASK_ID),
