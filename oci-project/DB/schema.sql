-- 15 DEPLOYMENTS
---------------------------------------------------------------
CREATE TABLE DEPLOYMENTS (
    DEPLOYMENT_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROJECT_ID        NUMBER,
    VERSION           VARCHAR2(50),
    ENVIRONMENT       VARCHAR2(50)
                      CHECK (ENVIRONMENT IN ('DEV','QA','STAGING','PRODUCTION')),
